                El citoplasma es la parte del protoplasma que, en una célula eucariota, se encuentra entre el núcleo celular y
                  la membrana plasmática. Consiste en una dispersión coloidal muy fina de aspecto granuloso, el citosol o hialoplasma,
                  y en una diversidad de orgánulos celulares que desempeñan diferentes funciones.
      Su función es albergar los orgánulos celulares y contribuir al movimiento de estos. El citosol es la sede de muchos de los procesos metabólicos que se dan en las células.
      El citoplasma se divide en ocasiones en una región externa gelatinosa, cercana a la membrana, e implicada en el movimiento celular, que se denomina ectoplasma; y una parte interna más fluida que recibe el nombre de endoplasma y donde se encuentran la mayoría de los orgánulos.
      El citoplasma se encuentra en las células procariotas así como en las eucariotas y en él se encuentran varios nutrientes que lograron atravesar la membrana plasmática, llegando de esta forma a los orgánulos de la célula.


      <h2>Ribosomas</h2>
      <p>Los ribosomas son gránulos citoplasmáticos encontrados en todas las células, y miden alrededor de 20 nm. Son portadores, además, de ARN ribosómico.</p>

      <h2>Lisosomas</h2>
      <p>Los lisosomas son vesículas esféricas,12 de entre 0,1 y 1 μm de diámetro. Contienen alrededor de 50 enzimas, generalmente hidrolíticas, en solución ácida; las enzimas necesitan esta solución ácida para un funcionamiento óptimo.13 Los lisosomas mantienen separadas a estas enzimas del resto de la célula, y así previenen que reaccionen químicamente con elementos y orgánulos de la célula .</p>

      <h2>Vacuolas</h2>
      <p>La vacuola es un saco de fluidos rodeado de una membrana. En la célula vegetal, la vacuola es una sola y de tamaño mayor; en cambio, en la célula animal, son varias y de tamaño reducido. La membrana que la rodea se denomina tonoplasto. La vacuola de la célula vegetal tiene una solución de sales minerales, azúcares, aminoácidos y a veces pigmentos como la antocianina.</p>

      <h2>Retículo endoplasmático</h2>
      <p>El retículo endoplasmático es un complejo sistema y conjunto de membranas conectadas entre sí,16 que forma un esqueleto citoplásmico. Forman un extenso sistema de canales y mantienen unidos a los ribosomas. Su forma puede variar, ya que su naturaleza depende del arreglo de células, que pueden estar comprimidas u organizadas de forma suelta.</p>

